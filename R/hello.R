#' a helper method to print name
#'
#' @return a string
#'
#' @param name input name
#' @export
#' @examples
#' hello("Bob")
hello <- function(name) {
  print(name)
}

#' A helper method to double an integer
#'
#' @param x
#'
#' @return doubled x
#' @export
#' @examples double(10) => 20
double <- function(x) {
  x*2
}


